    let currentLang = 'tr';

    function setLanguage(lang) {
      currentLang = lang;
      localStorage.setItem('bbt-lang', lang);
      document.documentElement.lang = lang;

      const t = translations[lang];
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) el.textContent = t[key];
      });
      document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key]) el.setAttribute('placeholder', t[key]);
      });

      const titles = {
        tr: 'BudgieBreedingTracker — Muhabbet Kuşu Üreme Takip Uygulaması',
        en: 'BudgieBreedingTracker — Budgie Breeding Tracker App',
        de: 'BudgieBreedingTracker — Wellensittich-Zucht-Tracker-App',
      };
      document.title = titles[lang];

      document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
      });
    }

    // ─── Mobile Menu ───
    function toggleMobileMenu() {
      const menu = document.getElementById('mobile-menu');
      const panel = document.getElementById('mobile-panel');
      const isOpen = menu.classList.contains('open');
      if (isOpen) { closeMobileMenu(); return; }
      menu.classList.remove('hidden');
      requestAnimationFrame(() => {
        menu.classList.add('open');
        panel.style.transform = 'translateY(0)';
      });
      document.getElementById('hamburger-icon').classList.add('hidden');
      document.getElementById('close-icon').classList.remove('hidden');
    }

    function closeMobileMenu() {
      const menu = document.getElementById('mobile-menu');
      const panel = document.getElementById('mobile-panel');
      menu.classList.remove('open');
      panel.style.transform = 'translateY(-110%)';
      document.getElementById('hamburger-icon').classList.remove('hidden');
      document.getElementById('close-icon').classList.add('hidden');
      setTimeout(() => menu.classList.add('hidden'), 300);
    }


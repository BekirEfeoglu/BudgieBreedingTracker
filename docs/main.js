    // ─── Email Signup ───
    function handleEmailSignup(e) {
      e.preventDefault();
      const email = document.getElementById('email-input').value.trim();
      const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
      if (!email || !emailPattern.test(email) || email.length > 254) return;
      const form = document.getElementById('email-form');
      const btn = form.querySelector('button[type="submit"]');
      const originalText = btn.textContent;
      btn.disabled = true;
      btn.textContent = '...';

      fetch('https://formsubmit.co/ajax/support@budgiebreedingtracker.online', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json', 'Accept': 'application/json' },
        body: JSON.stringify({
          email: email,
          _subject: 'BudgieBreedingTracker - Yeni Abone',
          _template: 'table',
        }),
      })
      .then(res => res.json())
      .then(data => {
        form.style.display = 'none';
        document.getElementById('email-thanks').classList.remove('hidden');
      })
      .catch(() => {
        // Fallback: still show success (email saved locally)
        form.style.display = 'none';
        document.getElementById('email-thanks').classList.remove('hidden');
      })
      .finally(() => {
        btn.disabled = false;
        btn.textContent = originalText;
      });
    }

    // ─── Stats Count-Up Animation ───
    function animateCount(el, target) {
      const duration = 1400;
      const suffix = el.textContent.replace(/[0-9]/g, '');
      const start = performance.now();
      function update(now) {
        const elapsed = now - start;
        const progress = Math.min(elapsed / duration, 1);
        const eased = 1 - Math.pow(1 - progress, 3);
        el.textContent = Math.round(eased * target) + suffix;
        if (progress < 1) requestAnimationFrame(update);
      }
      requestAnimationFrame(update);
    }

    // ─── Scroll Reveal (enhanced with stagger) ───
    function handleReveal() {
      const reveals = document.querySelectorAll('.reveal, .reveal-scale, .reveal-left, .reveal-right, .reveal-flip');
      const windowHeight = window.innerHeight;
      reveals.forEach(el => {
        const top = el.getBoundingClientRect().top;
        if (top < windowHeight - 60) {
          el.classList.add('active');
        }
      });
    }

    // ─── 3D Tilt on feature cards (enhanced) ───
    function initTilt() {
      document.querySelectorAll('.feature-card-enhanced').forEach(card => {
        card.addEventListener('mousemove', e => {
          const rect = card.getBoundingClientRect();
          const x = e.clientX - rect.left;
          const y = e.clientY - rect.top;
          const centerX = rect.width / 2;
          const centerY = rect.height / 2;
          const rotateX = ((y - centerY) / centerY) * -3;
          const rotateY = ((x - centerX) / centerX) * 3;
          // Mouse-follow spotlight
          const pctX = (x / rect.width * 100).toFixed(0);
          const pctY = (y / rect.height * 100).toFixed(0);
          card.style.setProperty('--mouse-x', pctX + '%');
          card.style.setProperty('--mouse-y', pctY + '%');
          card.style.transform = 'translateY(-8px) perspective(800px) rotateX(' + rotateX + 'deg) rotateY(' + rotateY + 'deg)';
        });
        card.addEventListener('mouseleave', () => {
          card.style.transform = '';
        });
      });
    }

    // ─── Navbar scroll effect ───
    function handleNavbar() {
      const navbar = document.getElementById('navbar');
      if (window.scrollY > 50) {
        navbar.classList.add('nav-scrolled');
      } else {
        navbar.classList.remove('nav-scrolled');
      }
    }

    // ─── Back to Top button ───
    function handleBackToTop() {
      const btn = document.getElementById('backToTop');
      if (window.scrollY > 400) {
        btn.classList.add('visible');
      } else {
        btn.classList.remove('visible');
      }
    }

    // ─── Scroll handler (throttled) ───
    let ticking = false;
    window.addEventListener('scroll', () => {
      if (!ticking) {
        window.requestAnimationFrame(() => {
          handleReveal();
          handleNavbar();
          handleBackToTop();
          // Scroll progress bar
          const scrollTop = document.documentElement.scrollTop;
          const scrollHeight = document.documentElement.scrollHeight - window.innerHeight;
          const bar = document.getElementById('scrollProgress');
          if (bar) bar.style.transform = 'scaleX(' + (scrollHeight > 0 ? scrollTop / scrollHeight : 0) + ')';
          ticking = false;
        });
        ticking = true;
      }
    });

    // ─── Init ───
    document.addEventListener('DOMContentLoaded', () => {
      // Language detection
      const saved = localStorage.getItem('bbt-lang');
      if (saved && translations[saved]) {
        setLanguage(saved);
      } else {
        const browserLang = (navigator.language || navigator.userLanguage || 'tr').substring(0, 2).toLowerCase();
        if (translations[browserLang]) {
          setLanguage(browserLang);
        }
      }

      // Initial reveal check
      setTimeout(handleReveal, 100);

      // Init 3D tilt on feature cards
      initTilt();

      // Stats count-up via IntersectionObserver (enhanced with underline reveal)
      const statsObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const el = entry.target;
            const target = parseInt(el.getAttribute('data-stat'), 10);
            if (!isNaN(target) && !el.hasAttribute('data-animated')) {
              el.setAttribute('data-animated', 'true');
              animateCount(el, target);
            }
            // Add counted class to parent for underline reveal
            const parent = el.closest('.stat-item-enhanced');
            if (parent) setTimeout(() => parent.classList.add('counted'), 600);
            statsObserver.unobserve(el);
          }
        });
      }, { threshold: 0.5 });
      document.querySelectorAll('[data-stat]').forEach(el => statsObserver.observe(el));

      // ─── Heading Reveal Observer ───
      const headingObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            entry.target.classList.add('active');
            headingObserver.unobserve(entry.target);
          }
        });
      }, { threshold: 0.5 });
      document.querySelectorAll('.heading-reveal').forEach(el => headingObserver.observe(el));

      // ─── Pause carousel when off-screen ───
      const carouselTrack = document.querySelector('.screenshot-track');
      if (carouselTrack) {
        const carouselObserver = new IntersectionObserver((entries) => {
          entries.forEach(e => carouselTrack.classList.toggle('paused', !e.isIntersecting));
        }, { threshold: 0 });
        carouselObserver.observe(carouselTrack.parentElement || carouselTrack);
      }

      // ─── Magnetic Buttons ───
      document.querySelectorAll('.magnetic-btn').forEach(btn => {
        btn.addEventListener('mousemove', (e) => {
          const rect = btn.getBoundingClientRect();
          const x = (e.clientX - rect.left - rect.width / 2) * 0.15;
          const y = (e.clientY - rect.top - rect.height / 2) * 0.15;
          btn.style.transform = 'translate(' + x + 'px, ' + y + 'px)';
        });
        btn.addEventListener('mouseleave', () => {
          btn.style.transform = 'translate(0, 0)';
        });
      });

    });

